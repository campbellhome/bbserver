b32 Style_ReadConfig(const char *colorscheme)
{
	Style_ResetConfig();

	b32 ret = false;
	for(u32 i = 0; i < BB_ARRAYSIZE(s_defaultStyleConfigs); ++i) {
		if(!bb_stricmp(colorscheme, s_defaultStyleConfigs[i].name)) {
			g_styleConfig = s_defaultStyleConfigs[i].colors;
			ret = true;
			break;
		}
	}

	return ret;
}

